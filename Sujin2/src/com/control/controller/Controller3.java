	public void array() {
		//저장된 학생 중 박씨성을 가진 학생 조회하기
		String[] names3= {"이동명","윤주","김태훈","오윤재","최이경","김수진","박세현","박보검","박상민"};
		for(String name:names3) {
			if(name.charAt(0)=='박') {
				System.out.println(name);
			}
		}
		
		
		//대문자와 소문자의 갯수를 구하시오
		int small=0;
		int large=0;
		
		char[] alpha2= {'a','b','D','E','T','s','y','U','h'};
		for(char alpha3:alpha2) {
			if(alpha3>='a'&&alpha3<='z') {
				small++;
			}
			if(alpha3>='A'&&alpha3<='Z') {
				large++;
			}
		}
		System.out.println(small+" "+large);
		
		//lotto만들기
		//1~45까지 번호를 6자리에 넣기
		//중복값을 허용하지 않음
		int[] lottoArr=new int[6];
//		int lotto=(int)(Math.random()*45+1);//for 안에 들어가야됨
		for(int i=0; i<lottoArr.length; i++) {
			int lotto=(int)(Math.random()*45+1);
			lottoArr[i]=lotto;
			for(int j=0; j<i; j++) {
				if(lottoArr[i]==lottoArr[j]) {
					i--;
					break;
				}
			}	
		}
//		System.out.println(Arrays.toString(lottoArr));
		for(int toLotto:lottoArr) {
			System.out.println(toLotto);
		}
		
	
		
		
		
		
		
	}
	public void upDown() {
		// up&down게임만들기
		// 랜덤으로 1~50까지의 수중 한개를 저장
		// 사용자에게 값을 입력받아 랜덤값과 비교하고 랜덤값이 크면 up출력
		// 작으면 Down출력 맞추면 정답입니다!! 짝짝짝! 출력 정답을 맞출때까지 진행!
		Scanner sc=new Scanner(System.in);
		
		
		int[] randomArr=new int[1];
		for(int i=0; i<randomArr.length; i++) {
			randomArr[i]=(int)(Math.random()*50+1);
		}
		System.out.println(randomArr[0]);
		
		System.out.println("1~50 입력하세요");
		
		char start='y';
		while(start=='y') {
		System.out.print("\n입력: ");
		int input=sc.nextInt();
		
		if(input<randomArr[0]) {
			System.out.print("up");
		}else if(input>randomArr[0]) {
			System.out.print("down");
		}else {
			System.out.print("정답입니다");
			start='n';
		}
		}	
	}
